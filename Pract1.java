package march16;
// Wap to swap two numbers

public class Pract1 {

	public static void main(String[] args) {
		int a=10;
		int b=20;
		System.out.println("a:- " + a +"," + "b:- " + b);
		int temp;
		temp=b;
		b=a;
		a=temp;
		System.out.println("a:- " + a +"," + "b:- " + b);

	}

}
